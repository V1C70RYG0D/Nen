#!/usr/bin/env node

/**
 * User Story 1: Real Devnet Demo & Test
 * 
 * Demonstrates the complete wallet connection flow with real devnet data
 * - Creates real wallet on devnet
 * - Airdrops real SOL to wallet
 * - Tests all 4 requirements with real on-chain data
 * - Validates User Story 1 acceptance criteria
 * 
 * Following GI.md: Real implementations only, no mocks
 */

const { Connection, PublicKey, Keypair, LAMPORTS_PER_SOL } = require('@solana/web3.js');
const nacl = require('tweetnacl');
const bs58 = require('bs58').default || require('bs58');

const DEVNET_RPC = process.env.SOLANA_RPC_URL || 'https://api.devnet.solana.com';
const BACKEND_URL = process.env.BACKEND_URL || 'http://localhost:3001';
const TEST_PROGRAM_ID = process.env.NEN_PROGRAM_ID || 'Fg6PaFpoGXkYsidMpWTK6W2BeZ7FEfcYkg476zPFsLnS';

class UserStory1DevnetDemo {
  constructor() {
    this.connection = new Connection(DEVNET_RPC, 'confirmed');
    this.testWallet = Keypair.generate();
    this.walletAddress = this.testWallet.publicKey.toString();
  }

  async runDemo() {
    console.log('üöÄ User Story 1: Real Devnet Demo');
    console.log('==================================================');
    console.log('üìç Devnet RPC:', DEVNET_RPC);
    console.log('üß™ Test Wallet:', this.walletAddress);
    console.log('üèóÔ∏è Program ID:', TEST_PROGRAM_ID);
    console.log('üñ•Ô∏è Backend URL:', BACKEND_URL);
    console.log('==================================================\\n');

    try {
      // Step 1: Demonstrate wallet connection workflow
      await this.demonstrateWalletConnection();
      
      // Step 2: Fund wallet with real devnet SOL
      await this.fundWalletWithDevnetSOL();
      
      // Step 3: Test all User Story 1 acceptance criteria
      await this.testAcceptanceCriteria();
      
      // Step 4: Demonstrate error handling scenarios
      await this.demonstrateErrorHandling();
      
      console.log('\\nüéâ User Story 1 Demo Completed Successfully!');
      console.log('‚úÖ All acceptance criteria validated with real devnet data');
      
    } catch (error) {
      console.error('üí• Demo failed:', error.message);
      process.exit(1);
    }
  }

  async demonstrateWalletConnection() {
    console.log('üëõ STEP 1: Wallet Connection Workflow');
    console.log('-'.repeat(50));
    
    // Simulate User clicks "Connect Wallet" button
    console.log('üñ±Ô∏è  User clicks "Connect Wallet" button');
    
    // Simulate User selects wallet provider (Phantom, Solflare, etc.)
    console.log('üì± User selects wallet provider (simulated)');
    
    // Generate signature to prove wallet ownership
    const timestamp = Date.now();
    const message = `Connect to Nen Platform: ${timestamp}`;
    const messageBytes = new TextEncoder().encode(message);
    const signature = nacl.sign.detached(messageBytes, this.testWallet.secretKey);
    const signatureBase58 = bs58.encode(signature);
    
    console.log('‚úçÔ∏è  User approves connection in wallet popup (simulated)');
    console.log(`üìù Signature created: ${signatureBase58.slice(0, 16)}...`);
    
    // Test backend authentication
    const fetch = (await import('node-fetch')).default;
    const authResponse = await fetch(`${BACKEND_URL}/api/auth/wallet`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        publicKey: this.walletAddress,
        signature: signatureBase58,
        message,
        timestamp
      })
    });
    
    if (authResponse.ok) {
      const authData = await authResponse.json();
      console.log('‚úÖ User sees wallet address displayed on platform');
      console.log(`   Wallet: ${authData.data.walletAddress}`);
      console.log(`   Verified: ${authData.data.verified}`);
      console.log(`   Network: ${authData.data.network}`);
    } else {
      throw new Error('Wallet authentication failed');
    }
  }

  async fundWalletWithDevnetSOL() {
    console.log('\\nüí∞ STEP 2: Fund Wallet with Real Devnet SOL');
    console.log('-'.repeat(50));
    
    try {
      console.log('üí≥ Requesting devnet SOL airdrop...');
      
      // Request airdrop of 2 SOL for testing
      const airdropSignature = await this.connection.requestAirdrop(
        this.testWallet.publicKey,
        2 * LAMPORTS_PER_SOL
      );
      
      console.log(`üåßÔ∏è  Airdrop requested: ${airdropSignature.slice(0, 16)}...`);
      
      // Confirm the airdrop transaction
      const latestBlockhash = await this.connection.getLatestBlockhash();
      await this.connection.confirmTransaction({
        signature: airdropSignature,
        ...latestBlockhash
      });
      
      // Verify balance
      const balance = await this.connection.getBalance(this.testWallet.publicKey);
      const balanceSOL = balance / LAMPORTS_PER_SOL;
      
      console.log(`‚úÖ Airdrop confirmed! Balance: ${balanceSOL} SOL`);
      console.log(`üîó View on Explorer: https://explorer.solana.com/tx/${airdropSignature}?cluster=devnet`);
      
    } catch (error) {
      console.log('‚ö†Ô∏è  Airdrop failed (rate limits are common), proceeding with 0 balance...');
      console.log(`   Error: ${error.message}`);
    }
  }

  async testAcceptanceCriteria() {
    console.log('\\nüìã STEP 3: Testing User Story 1 Acceptance Criteria');
    console.log('-'.repeat(50));
    
    const fetch = (await import('node-fetch')).default;
    
    // Test: User sees wallet address displayed on platform
    console.log('üß™ Testing: User sees wallet address displayed');
    const currentBalance = await this.connection.getBalance(this.testWallet.publicKey);
    console.log(`‚úÖ Wallet Address: ${this.walletAddress}`);
    console.log(`‚úÖ Current Balance: ${currentBalance / LAMPORTS_PER_SOL} SOL`);
    
    // Test: Platform account PDA initialization
    console.log('\\nüß™ Testing: Platform account PDA initialization');
    const initResponse = await fetch(`${BACKEND_URL}/api/user/check-and-initialize`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        walletAddress: this.walletAddress,
        options: { autoInitialize: true, kycLevel: 0, region: 0 }
      })
    });
    
    if (initResponse.ok) {
      const initData = await initResponse.json();
      console.log(`‚úÖ Platform Account PDA: ${initData.data.userAccountPda}`);
      console.log(`‚úÖ Account exists: ${initData.data.accountExists}`);
      console.log(`‚úÖ Initialization: ${initData.data.initialized ? 'SUCCESS' : 'READY'}`);
    }
    
    // Test: Balance query and display
    console.log('\\nüß™ Testing: Balance query and display');
    const balanceResponse = await fetch(`${BACKEND_URL}/api/user/balance?wallet=${this.walletAddress}`);
    
    if (balanceResponse.ok) {
      const balanceData = await balanceResponse.json();
      console.log(`‚úÖ Backend Balance Query: ${balanceData.data.balance.formatted}`);
      console.log(`‚úÖ Network: ${balanceData.data.network}`);
      console.log(`‚úÖ Last Updated: ${balanceData.data.lastUpdated}`);
    }
    
    // Test: PDA consistency check
    console.log('\\nüß™ Testing: PDA consistency between frontend and backend');
    const pdaResponse = await fetch(`${BACKEND_URL}/api/user/check-pda`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ walletAddress: this.walletAddress })
    });
    
    if (pdaResponse.ok) {
      const pdaData = await pdaResponse.json();
      
      // Verify PDA derivation locally
      const programId = new PublicKey(TEST_PROGRAM_ID);
      const [localPDA] = PublicKey.findProgramAddressSync(
        [Buffer.from('user'), this.testWallet.publicKey.toBuffer()],
        programId
      );
      
      const isConsistent = pdaData.data.accountAddress === localPDA.toString();
      console.log(`‚úÖ Backend PDA: ${pdaData.data.accountAddress}`);
      console.log(`‚úÖ Local PDA: ${localPDA.toString()}`);
      console.log(`‚úÖ Consistency: ${isConsistent ? 'VERIFIED' : 'FAILED'}`);
    }
  }

  async demonstrateErrorHandling() {
    console.log('\\nüö® STEP 4: Error Handling Scenarios');
    console.log('-'.repeat(50));
    
    const fetch = (await import('node-fetch')).default;
    
    // Test: Invalid signature
    console.log('üß™ Testing: Invalid signature rejection');
    const invalidAuthResponse = await fetch(`${BACKEND_URL}/api/auth/wallet`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        publicKey: this.walletAddress,
        signature: 'invalid_signature',
        message: 'test message',
        timestamp: Date.now()
      })
    });
    
    if (!invalidAuthResponse.ok) {
      console.log('‚úÖ Invalid signature properly rejected');
    } else {
      console.log('‚ùå Invalid signature should have been rejected');
    }
    
    // Test: Missing required fields
    console.log('\\nüß™ Testing: Missing required fields handling');
    const incompleteResponse = await fetch(`${BACKEND_URL}/api/auth/wallet`, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ publicKey: this.walletAddress })
    });
    
    if (!incompleteResponse.ok) {
      console.log('‚úÖ Missing fields properly handled');
    } else {
      console.log('‚ùå Missing fields should have been rejected');
    }
    
    // Test: Invalid wallet address format
    console.log('\\nüß™ Testing: Invalid wallet address handling');
    const invalidWalletResponse = await fetch(`${BACKEND_URL}/api/user/balance?wallet=invalid_address`);
    
    if (!invalidWalletResponse.ok) {
      console.log('‚úÖ Invalid wallet address properly handled');
    } else {
      console.log('‚ùå Invalid wallet address should have been rejected');
    }
  }
}

// Run the demo
if (require.main === module) {
  const demo = new UserStory1DevnetDemo();
  demo.runDemo().catch(error => {
    console.error('üí• Demo failed:', error);
    process.exit(1);
  });
}

module.exports = { UserStory1DevnetDemo };
